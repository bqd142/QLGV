    private void btthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemActionPerformed
        DAL_TGDT tmp = new DAL_TGDT(cbbmagv.getSelectedItem().toString(), cbbmadt.getSelectedItem().toString(), cbbstt.getSelectedItem().toString(),txtphucap.getText(), txtkq.getText());
        boolean kq = tmp.Insert(tmp);
        if(kq){
            cv.update_tt(cbbmadt.getSelectedItem().toString(), cbbstt.getSelectedItem().toString());
            JOptionPane.showMessageDialog(null,"Insert success");
        }
        else JOptionPane.showMessageDialog(null,"Insert fail");
    }//GEN-LAST:event_btthemActionPerformed

