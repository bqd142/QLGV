    public THAMGIADT() {
        initComponents();
        tbtgdt.setModel(tgdt.Select());
        for(int i = 0; i < gv.Select().getRowCount(); i++){
            cbbmagv.addItem(gv.Select().getValueAt(i, 0).toString());
        }
        for(int i = 0; i < cv.Select().getRowCount(); i++){
            String mdt = cv.Select().getValueAt(i, 0).toString();
            int kt = 0;
            for(int j = 0; j < cbbmadt.getItemCount(); j++){
                if(mdt.equalsIgnoreCase(cbbmadt.getItemAt(j))){
                   kt++;
                } 
            }
            if(kt == 0){
                cbbmadt.addItem(mdt);
            }
            
        }
    }
    public void click(){
        int row = tbtgdt.getSelectedRow();
        String mgv = tbtgdt.getValueAt(row, 0).toString();
        String mdt = tbtgdt.getValueAt(row, 1).toString();
        String stt = tbtgdt.getValueAt(row, 2).toString();
        String phucap = tbtgdt.getValueAt(row, 3).toString();
        String kq = tbtgdt.getValueAt(row, 4).toString();
        cbbmagv.setSelectedItem(mgv);
        cbbmadt.setSelectedItem(mdt);
        cbbstt.addItem(stt);
        cbbstt.setSelectedItem(stt);
        txtphucap.setText(phucap);
        txtkq.setText(kq);
        madtcu = mdt;
        sttcu = stt;
    }

